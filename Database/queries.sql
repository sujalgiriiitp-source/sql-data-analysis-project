-- 51. (CASE WHEN) Customer Segmentation based on Lifetime Value (LTV)
SELECT c.customer_id, c.first_name, SUM(o.total_amount) AS ltv,
       CASE 
           WHEN SUM(o.total_amount) > 50000 THEN 'Platinum'
           WHEN SUM(o.total_amount) BETWEEN 10000 AND 50000 THEN 'Gold'
           WHEN SUM(o.total_amount) BETWEEN 1000 AND 9999 THEN 'Silver'
           ELSE 'Bronze'
       END AS customer_tier
FROM Customers c
JOIN Orders o ON c.customer_id = o.customer_id
GROUP BY c.customer_id, c.first_name;

-- 52. View Creation: Create a View for Daily Sales Report
CREATE OR REPLACE VIEW Daily_Sales_Report AS
SELECT CAST(order_date AS DATE) AS sale_date, 
       COUNT(order_id) AS total_orders, 
       SUM(total_amount) AS total_revenue
FROM Orders
WHERE order_status = 'Delivered'
GROUP BY CAST(order_date AS DATE);

-- 53. Query the view
SELECT * FROM Daily_Sales_Report ORDER BY sale_date DESC LIMIT 10;

-- 54. String Aggregation: List all products bought by a specific customer (e.g. customer_id = 5)
SELECT o.customer_id, STRING_AGG(p.product_name, ', ') AS products_bought
FROM Orders o
JOIN Order_Items oi ON o.order_id = oi.order_id
JOIN Products p ON oi.product_id = p.product_id
WHERE o.customer_id = 5
GROUP BY o.customer_id;

-- 55. Data Cleaning Simulation: Find customers with invalid email formats using Regex
SELECT customer_id, email
FROM Customers
WHERE email !~ '^[A-Za-z0-9._%-]+@[A-Za-z0-9.-]+[.][A-Za-z]+$';
