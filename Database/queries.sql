-- 41. (ROW_NUMBER) Find the first order placed by each customer
WITH RankedOrders AS (
    SELECT customer_id, order_id, order_date,
           ROW_NUMBER() OVER(PARTITION BY customer_id ORDER BY order_date ASC) as rn
    FROM Orders
)
SELECT customer_id, order_id, order_date FROM RankedOrders WHERE rn = 1;

-- 42. (RANK) Rank products within each category based on price
SELECT category_id, product_name, price,
       RANK() OVER(PARTITION BY category_id ORDER BY price DESC) as price_rank
FROM Products;

-- 43. (DENSE_RANK) Rank employees by salary within their departments
SELECT department, first_name, salary,
       DENSE_RANK() OVER(PARTITION BY department ORDER BY salary DESC) as salary_rank
FROM Employees;

-- 44. (LAG) Calculate month-over-month (MoM) revenue growth
WITH MonthlyRev AS (
    SELECT DATE_TRUNC('month', order_date) AS order_month, SUM(total_amount) AS revenue
    FROM Orders WHERE order_status = 'Delivered'
    GROUP BY DATE_TRUNC('month', order_date)
)
SELECT order_month, revenue,
       LAG(revenue) OVER(ORDER BY order_month) AS prev_month_rev,
       ROUND(((revenue - LAG(revenue) OVER(ORDER BY order_month)) / LAG(revenue) OVER(ORDER BY order_month)) * 100, 2) AS mom_growth_pct
FROM MonthlyRev;

-- 45. (LEAD) Find the time gap between a customer's current order and their next order
WITH CustomerOrders AS (
    SELECT customer_id, order_id, order_date,
           LEAD(order_date) OVER(PARTITION BY customer_id ORDER BY order_date) AS next_order_date
    FROM Orders
)
SELECT customer_id, order_id, order_date, next_order_date,
       EXTRACT(DAY FROM (next_order_date - order_date)) AS days_until_next_order
FROM CustomerOrders;

-- 46. (Running Total) Calculate the cumulative revenue over time
SELECT order_date, total_amount,
       SUM(total_amount) OVER(ORDER BY order_date) AS cumulative_revenue
FROM Orders
WHERE order_status = 'Delivered';

-- 47. Find the top 2 highest selling products in each category (using ROW_NUMBER)
WITH CategorySales AS (
    SELECT p.category_id, p.product_name, SUM(oi.quantity) as qty_sold,
           ROW_NUMBER() OVER(PARTITION BY p.category_id ORDER BY SUM(oi.quantity) DESC) as rn
    FROM Products p
    JOIN Order_Items oi ON p.product_id = oi.product_id
    GROUP BY p.category_id, p.product_name
)
SELECT category_id, product_name, qty_sold FROM CategorySales WHERE rn <= 2;

-- 48. NTILE - Segment customers into 4 quartiles based on their total spending
WITH CustomerSpend AS (
    SELECT customer_id, SUM(total_amount) AS total_spent
    FROM Orders GROUP BY customer_id
)
SELECT customer_id, total_spent,
       NTILE(4) OVER(ORDER BY total_spent DESC) AS spending_quartile
FROM CustomerSpend;

-- 49. (Moving Average) Calculate a 7-day moving average of daily revenue
WITH DailyRev AS (
    SELECT CAST(order_date AS DATE) AS d_date, SUM(total_amount) AS daily_revenue
    FROM Orders GROUP BY CAST(order_date AS DATE)
)
SELECT d_date, daily_revenue,
       AVG(daily_revenue) OVER(ORDER BY d_date ROWS BETWEEN 6 PRECEDING AND CURRENT ROW) AS moving_avg_7d
FROM DailyRev;

-- 50. Percent_Rank of products based on price
SELECT product_name, price,
       ROUND(CAST(PERCENT_RANK() OVER(ORDER BY price) AS NUMERIC), 2) AS price_percentile
FROM Products;


