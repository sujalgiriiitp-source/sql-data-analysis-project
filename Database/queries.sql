-- SECTION 4: SUBQUERIES & CTEs
-- ==========================================

-- 31. (Subquery) Find products with a price higher than the average product price
SELECT product_name, price 
FROM Products 
WHERE price > (SELECT AVG(price) FROM Products);

-- 32. (Correlated Subquery) Find customers whose latest order was placed in the last 10 days
SELECT customer_id, first_name 
FROM Customers c
WHERE EXISTS (
    SELECT 1 
    FROM Orders o 
    WHERE o.customer_id = c.customer_id 
    AND order_date >= CURRENT_DATE - INTERVAL '10 days'
);

-- 33. (Subquery in FROM) Calculate the average number of orders per customer
SELECT AVG(order_count) AS avg_orders_per_customer
FROM (
    SELECT customer_id, COUNT(order_id) AS order_count
    FROM Orders
    GROUP BY customer_id
) sub;

-- 34. (CTE) Find the Top 5 Customers by Lifetime Value (LTV)
WITH CustomerRevenue AS (
    SELECT customer_id, SUM(total_amount) AS lifetime_value
    FROM Orders
    WHERE order_status = 'Delivered'
    GROUP BY customer_id
)
SELECT c.first_name, c.last_name, cr.lifetime_value
FROM Customers c
JOIN CustomerRevenue cr ON c.customer_id = cr.customer_id
ORDER BY cr.lifetime_value DESC
LIMIT 5;

-- 35. (CTE) Monthly Active Customers (MAC)
WITH MonthlyCustomers AS (
    SELECT EXTRACT(MONTH FROM order_date) AS month, 
           EXTRACT(YEAR FROM order_date) AS year,
           COUNT(DISTINCT customer_id) AS active_customers
    FROM Orders
    GROUP BY EXTRACT(YEAR FROM order_date), EXTRACT(MONTH FROM order_date)
)
SELECT year, month, active_customers 
FROM MonthlyCustomers 
ORDER BY year, month;

-- 36. (Recursive CTE) Generate an Employee Hierarchy (Org Chart)
WITH RECURSIVE OrgChart AS (
    -- Anchor member: Start with top-level managers
    SELECT employee_id, first_name, manager_id, 1 AS level
    FROM Employees
    WHERE manager_id IS NULL
    
    UNION ALL
    
    -- Recursive member: Find employees managed by the previous level
    SELECT e.employee_id, e.first_name, e.manager_id, oc.level + 1
    FROM Employees e
    INNER JOIN OrgChart oc ON e.manager_id = oc.employee_id
)
SELECT * FROM OrgChart ORDER BY level, manager_id;

-- 37. Find products that have never been ordered
SELECT product_id, product_name 
FROM Products 
WHERE product_id NOT IN (SELECT DISTINCT product_id FROM Order_Items);

-- 38. Find the most recent order for each customer using a Correlated Subquery
SELECT o1.customer_id, o1.order_id, o1.order_date
FROM Orders o1
WHERE o1.order_date = (
    SELECT MAX(order_date) 
    FROM Orders o2 
    WHERE o2.customer_id = o1.customer_id
);

-- 39. (CTE) Identify customers with abandoned carts (Simulated: Pending orders without successful payment)
WITH AbandonedOrders AS (
    SELECT o.order_id, o.customer_id
    FROM Orders o
    LEFT JOIN Payments p ON o.order_id = p.order_id
    WHERE o.order_status = 'Pending' AND (p.payment_status != 'Success' OR p.payment_id IS NULL)
)
SELECT c.first_name, c.email, a.order_id 
FROM Customers c 
JOIN AbandonedOrders a ON c.customer_id = a.customer_id;

-- 40. Determine the percentage contribution of each category to total revenue
WITH CategoryRevenue AS (
    SELECT c.category_name, SUM(oi.total_price) AS revenue
    FROM Categories c
    JOIN Products p ON c.category_id = p.category_id
    JOIN Order_Items oi ON p.product_id = oi.product_id
    GROUP BY c.category_name
), TotalRevenue AS (
    SELECT SUM(revenue) AS total FROM CategoryRevenue
)
SELECT cr.category_name, cr.revenue, 
       ROUND((cr.revenue / tr.total) * 100, 2) AS percentage_contribution
FROM CategoryRevenue cr, TotalRevenue tr
ORDER BY percentage_contribution DESC;


