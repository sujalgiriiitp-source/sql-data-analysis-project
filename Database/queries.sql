-- 1. Retrieve all active customers from Maharashtra
SELECT customer_id, first_name, last_name, city, state 
FROM Customers 
WHERE state = 'Maharashtra' 
ORDER BY city;

-- 2. Find all products that cost more than ₹5000 and are in stock
SELECT product_id, product_name, price, stock_quantity 
FROM Products 
WHERE price > 5000 AND stock_quantity > 0;

-- 3. Get a unique list of all delivery cities where orders were shipped
SELECT DISTINCT delivery_city 
FROM Orders 
WHERE order_status IN ('Shipped', 'Delivered');

-- 4. Find all customers who registered in the year 2023
SELECT customer_id, first_name, email, registration_date 
FROM Customers 
WHERE EXTRACT(YEAR FROM registration_date) = 2023;

-- 5. List the top 10 most expensive products
SELECT product_name, price 
FROM Products 
ORDER BY price DESC 
LIMIT 10;

-- 6. Find all orders placed in the last 30 days (simulated as from current_date)
SELECT order_id, order_date, total_amount, order_status
FROM Orders
WHERE order_date >= CURRENT_DATE - INTERVAL '30 days';

-- 7. Get the details of all pending payments
SELECT payment_id, order_id, amount, payment_mode
FROM Payments
WHERE payment_status = 'Pending';

-- 8. List employees who don't have a manager (Top Level Executives)
SELECT employee_id, first_name, last_name, department
FROM Employees
WHERE manager_id IS NULL;

-- 9. Find all suppliers located in Delhi or Karnataka
SELECT supplier_name, city, state
FROM Suppliers
WHERE state IN ('Delhi', 'Karnataka');

-- 10. Search for products that have the word 'Smart' in their name
SELECT product_id, product_name, price
FROM Products
WHERE product_name ILIKE '%Smart%';


-- ==========================================
-- SECTION 2: AGGREGATION & GROUPING (GROUP BY, HAVING)
-- ==========================================

-- 11. Count the number of customers in each state
SELECT state, COUNT(customer_id) AS total_customers
FROM Customers
GROUP BY state
ORDER BY total_customers DESC;

-- 12. Find the total revenue generated by the platform (Success payments only)
SELECT SUM(amount) AS total_revenue
FROM Payments
WHERE payment_status = 'Success';

-- 13. Calculate the average order value (AOV)
SELECT ROUND(AVG(total_amount), 2) AS average_order_value
FROM Orders
WHERE order_status != 'Cancelled';

-- 14. Find categories with more than 50 products
SELECT category_id, COUNT(product_id) AS product_count
FROM Products
GROUP BY category_id
HAVING COUNT(product_id) > 50;

-- 15. Get the maximum and minimum salary in each department
SELECT department, MAX(salary) AS highest_salary, MIN(salary) AS lowest_salary
FROM Employees
GROUP BY department;

-- 16. Monthly revenue trend for the year 2023
SELECT EXTRACT(MONTH FROM order_date) AS month, SUM(total_amount) AS monthly_revenue
FROM Orders
WHERE EXTRACT(YEAR FROM order_date) = 2023 AND order_status IN ('Delivered', 'Shipped')
GROUP BY EXTRACT(MONTH FROM order_date)
ORDER BY month;

-- 17. Count of orders by order status
SELECT order_status, COUNT(order_id) AS number_of_orders
FROM Orders
GROUP BY order_status;

-- 18. Find the total quantity sold for each product
SELECT product_id, SUM(quantity) AS total_quantity_sold
FROM Order_Items
GROUP BY product_id
ORDER BY total_quantity_sold DESC;

-- 19. Identify the top 5 most popular payment modes
SELECT payment_mode, COUNT(payment_id) AS transaction_count
FROM Payments
GROUP BY payment_mode
ORDER BY transaction_count DESC
LIMIT 5;

-- 20. Find states generating more than ₹1,000,000 in revenue
SELECT delivery_state, SUM(total_amount) AS state_revenue
FROM Orders
WHERE order_status = 'Delivered'
GROUP BY delivery_state
HAVING SUM(total_amount) > 1000000
ORDER BY state_revenue DESC;


