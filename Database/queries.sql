-- SECTION 3: JOINS (INNER, LEFT, RIGHT, FULL, SELF, CROSS)
-- ==========================================

-- 21. (INNER JOIN) List order details along with customer names
SELECT o.order_id, c.first_name, c.last_name, o.order_date, o.total_amount
FROM Orders o
INNER JOIN Customers c ON o.customer_id = c.customer_id;

-- 22. (LEFT JOIN) Find all customers and their orders, including customers with zero orders
SELECT c.customer_id, c.first_name, COUNT(o.order_id) AS total_orders
FROM Customers c
LEFT JOIN Orders o ON c.customer_id = o.customer_id
GROUP BY c.customer_id, c.first_name;

-- 23. (RIGHT JOIN) List all products and their category names, ensuring all categories are shown
SELECT p.product_name, c.category_name
FROM Products p
RIGHT JOIN Categories c ON p.category_id = c.category_id;

-- 24. (FULL JOIN) Find mismatch between orders and payments (e.g. order exists but no payment record)
SELECT o.order_id, p.payment_id, o.total_amount, p.amount
FROM Orders o
FULL JOIN Payments p ON o.order_id = p.order_id
WHERE o.order_id IS NULL OR p.payment_id IS NULL;

-- 25. (SELF JOIN) List employees and the names of their managers
SELECT e.first_name AS employee_name, m.first_name AS manager_name
FROM Employees e
LEFT JOIN Employees m ON e.manager_id = m.employee_id;

-- 26. (CROSS JOIN) Create a combination of all categories and order statuses for a matrix report
SELECT c.category_name, os.status
FROM Categories c
CROSS JOIN (SELECT DISTINCT order_status AS status FROM Orders) os;

-- 27. Multiple Joins: Get complete order receipt (Order ID, Customer Name, Product Name, Qty, Price)
SELECT o.order_id, c.first_name, p.product_name, oi.quantity, oi.unit_price, oi.total_price
FROM Orders o
JOIN Customers c ON o.customer_id = c.customer_id
JOIN Order_Items oi ON o.order_id = oi.order_id
JOIN Products p ON oi.product_id = p.product_id
WHERE o.order_id = 1001; -- Replace 1001 with specific order_id

-- 28. Find the total revenue generated by each category
SELECT c.category_name, SUM(oi.total_price) AS revenue
FROM Categories c
JOIN Products p ON c.category_id = p.category_id
JOIN Order_Items oi ON p.product_id = oi.product_id
JOIN Orders o ON oi.order_id = o.order_id
WHERE o.order_status = 'Delivered'
GROUP BY c.category_name
ORDER BY revenue DESC;

-- 29. Find the supplier for the most sold product
SELECT s.supplier_name, p.product_name, SUM(oi.quantity) AS total_sold
FROM Suppliers s
JOIN Products p ON s.supplier_id = p.supplier_id
JOIN Order_Items oi ON p.product_id = oi.product_id
GROUP BY s.supplier_name, p.product_name
ORDER BY total_sold DESC
LIMIT 1;

-- 30. Find customers who have purchased products from 'Electronics' category
SELECT DISTINCT c.first_name, c.last_name, c.email
FROM Customers c
JOIN Orders o ON c.customer_id = o.customer_id
JOIN Order_Items oi ON o.order_id = oi.order_id
JOIN Products p ON oi.product_id = p.product_id
JOIN Categories cat ON p.category_id = cat.category_id
WHERE cat.category_name = 'Electronics';


